package model.dto;

public class ClassEnroll {
	
	private int class_enroll;
	private int staff_id;
	private int sub_id;
	private int class_id;
	
	public ClassEnroll() {
		
	}

	/**
	 * @return the class_enroll
	 */
	public int getClass_enroll() {
		return class_enroll;
	}

	/**
	 * @param class_enroll the class_enroll to set
	 */
	public void setClass_enroll(int class_enroll) {
		this.class_enroll = class_enroll;
	}

	/**
	 * @return the staff_id
	 */
	public int getStaff_id() {
		return staff_id;
	}

	/**
	 * @param staff_id the staff_id to set
	 */
	public void setStaff_id(int staff_id) {
		this.staff_id = staff_id;
	}

	/**
	 * @return the sub_id
	 */
	public int getSub_id() {
		return sub_id;
	}

	/**
	 * @param sub_id the sub_id to set
	 */
	public void setSub_id(int sub_id) {
		this.sub_id = sub_id;
	}

	/**
	 * @return the class_id
	 */
	public int getClass_id() {
		return class_id;
	}

	/**
	 * @param class_id the class_id to set
	 */
	public void setClass_id(int class_id) {
		this.class_id = class_id;
	}

}
