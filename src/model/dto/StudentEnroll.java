package model.dto;

public class StudentEnroll {

	private int stu_enroll;
	private int stu_id;
	private int class_id;
	private String description;
	/**
	 * @return the stu_enroll
	 */
	public int getStu_enroll() {
		return stu_enroll;
	}
	/**
	 * @param stu_enroll the stu_enroll to set
	 */
	public void setStu_enroll(int stu_enroll) {
		this.stu_enroll = stu_enroll;
	}
	/**
	 * @return the stu_id
	 */
	public int getStu_id() {
		return stu_id;
	}
	/**
	 * @param stu_id the stu_id to set
	 */
	public void setStu_id(int stu_id) {
		this.stu_id = stu_id;
	}
	/**
	 * @return the class_id
	 */
	public int getClass_id() {
		return class_id;
	}
	/**
	 * @param class_id the class_id to set
	 */
	public void setClass_id(int class_id) {
		this.class_id = class_id;
	}
	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}
	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}
	
	
}
